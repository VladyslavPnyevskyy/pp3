import java.lang.Math;
public class MyMath {
    public Integer greater(int a, int b){
        return Math.max(a,b);
    }
    public Double pi(){
        return Math.PI;
    }
    public Integer absolute(int a){
return Math.abs(a);
    }
    public Double randm(){
        return Math.random();
    }
    public Integer randomTen(){
        double int_r =  Math.random()*10;
        return (int)int_r;
    }
    public Double sine(){
        return Math.sin(90);
    }
}
